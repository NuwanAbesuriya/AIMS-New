<script>
    $(document).ready(function() {
        // Handle show more button clicks
        $('.show-more').click(function() {
            var newsId = $(this).data('news-id');
            var imageUrl = $(this).closest('.card').find('img').attr('src');
            var title = $(this).closest('.card').find('.card-title').text();
            
            // Send an AJAX request to get the descriptions from the database
            $.ajax({
                url: '../controller/get_descriptions.php',
                type: 'POST',
                data: { 'news_id': newsId },
                success: function(descriptions) {
                    $('#newsImage').attr('src', imageUrl);
                    $('#newsTitle').text(title);
                    $('#newsdescriptions').text(descriptions);

                    $('#newsModal').modal('show');
                },
                error: function(error) {
                    console.error(error);
                }
            });
        });
    });
</script>




























